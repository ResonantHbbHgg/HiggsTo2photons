#     """ returns all known masses. Note that not all processes are available at all masses """

def getXsect(mass, processes = [ 'ggf', 'vbf', 'wh', 'zh', 'tth' ]):
    """ if more than one process is specified, sums the contributions.

        Does NOT interpolate between masses
    """

    # check whether the value of processes is a list or a single item
    if not hasattr(processes,"__iter__"):
        processes = [ processes ]

    retval = 0

    for proc in processes:
        xsect = None

        proc = proc.lower()

        if proc == 'ggf' or proc == 'ggh':
            xsect = ggfXsect
        elif proc == 'vbf' or proc == 'vvh':
            xsect = vbfXsect
        elif proc == 'wh':
            xsect = whXsect
        elif proc == 'zh':
            xsect = zhXsect
        elif proc == 'tth':
            xsect = tthXsect

        retval += xsect[xsect['mass'] == mass]['xsect'][0]

    return retval


#----------------------------------------------------------------------
def getFermiophobicXsect(mass):
    return getXsect(mass, ['vbf','wh','zh'])

