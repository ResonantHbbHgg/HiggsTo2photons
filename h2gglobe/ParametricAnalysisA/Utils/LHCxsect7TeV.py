def _convertFromText(text, colNames):
    # first find out how many lines there are
    lines = [ line.strip() for line in text.splitlines() ]
    lines = [ line for line in lines if line != "" ]

    data = numpy.zeros(len(lines), dtype=( zip(colNames, [ 'f8' ] * len(colNames) )))

    for index, line in enumerate(lines):
        values = [ float(x) for x in re.split('\s+',line) ]

        for colName, value in zip(colNames,values):
            data[colName][index] = value

    return data

